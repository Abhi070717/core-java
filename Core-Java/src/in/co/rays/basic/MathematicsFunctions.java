package in.co.rays.basic;

public class MathematicsFunctions {
	public static void main(String[] args) {
		System.out.println("Mathematics Function");
		System.out.println("Maximum 2,5 : " + Math.max(2, 5));
		System.out.println("minimum 2,5 : " + Math.min(2, 5));
		System.out.println("Absolute 3.7 : " + Math.abs(3.7));
		System.out.println("Exp 10 : " + Math.exp(10));
		System.out.println("Random Number : " + Math.random());
		System.out.println("Square Root : " + Math.sqrt(4));
	}
	
}
